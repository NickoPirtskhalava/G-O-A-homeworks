name1 = float(input("შეიყვანეთ პირველი რიცხვი (name1): "))
name2 = float(input("შეიყვანეთ მეორე რიცხვი (name2): "))

print("ჯამი (+): " + str(name1 + name2))
print("განსხვავება (-): " + str(name1 - name2))
print("მთელი გაყოფა (//): " + str(name1 // name2))
print("გაყოფა (/): " + str(name1 / name2))
print("გამრავლება (*): " + str(name1 * name2))
print("ხარისხში აყვანა (**): " + str(name1 ** name2))